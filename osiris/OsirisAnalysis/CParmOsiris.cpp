bool CParmOsiris::Save()
{
  bool bRtn = SaveFile(m_sFileName);
  if(!bRtn)
  {
    wxString sError =
      _T("An error occurred when writing OSIRS parameters file:\n");
    sError.Append(m_sFileName);
    wxASSERT_MSG(0,sError);
    mainApp::LogMessage(sError);
  }
  else
  {
    m_bModified = false;
  }
  return bRtn;
}

